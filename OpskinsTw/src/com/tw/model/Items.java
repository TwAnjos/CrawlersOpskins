package com.tw.model;

/**
 *
 * @author Tw
 */
public class Items {
    
    private String nome;
    private String link;
    private float sop;
    private float sugerido;
    private float lucroop;
    private float lucroreais;
    private float dollaratual;

    public float getDollaratual() {
        return dollaratual;
    }

    public void setDollaratual(float dollaratual) {
        this.dollaratual = dollaratual;
    }
    

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public float getSop() {
        return sop;
    }

    public void setSop(float sop) {
        this.sop = sop;
    }

    public float getSugerido() {
        return sugerido;
    }

    public void setSugerido(float sugerido) {
        this.sugerido = sugerido;
    }

    public float getLucroop() {
        return lucroop;
    }

    public void setLucroop(float lucroop) {
        this.lucroop = lucroop;
    }

    public float getLucroreais() {
        return lucroreais;
    }

    public void setLucroreais(float lucroreais) {
        this.lucroreais = lucroreais;
    }
    
    
   
    
}
